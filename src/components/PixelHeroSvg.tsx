/**
 * Habitica風ピクセルアートキャラクター
 * ランディング画面・ログイン画面用
 */
export default function PixelHeroSvg({ type, size = 60 }: Props) {
  const pixels = type === "warrior" ? WARRIOR_PIXELS : MAGE_PIXELS;
  const gridW = 13;
  const gridH = 13;
  const vw = gridW * PX;
  const vh = gridH * PX;

  return (
    <Svg width={size} height={size} viewBox={`0 0 ${vw} ${vh}`}>
      <G>
        {pixels.map(([x, y, color], i) => (
          <Rect
            key={i}
            x={x * PX}
            y={y * PX}
            width={PX}
            height={PX}
            fill={color}
          />
        ))}
      </G>
    </Svg>
  );
}
